# operators     description
# ==            if the values of two operands are equal, then the condition become true
# !=            if values of two operands are not equal, then the condition become true
# >             if the value of left operand is greater than the value of right operand, then the condition is true
# <             if the value of left operand is less than the value of right operand, then the condition is true
# >=            if the value of left operand is greater or equal to right operand, then the condition is true
# <=            if the value of left operand is less or equal to right operand, then the condition is true

print(1==1) # print True

print(1!=1) # print False
print(1!=10) # print True

print(5>3) # print True
print(6>9) # print False

print(4<7) # print True
print(6<5) # print False

print(4>=4) # print True
print(6>=8) # print False

print(3<=3) # print True
print(3<=2) # print False
